-(BOOL)isEmpty {
    NSLog(@"self->>>>>.%@", self);
    if (self.length == 0 || [self isKindOfClass: [NSNull class]] || self == nil || [self isEqualToString:@"nil"] || [self isEqualToString:@"null"] || [self isEqualToString:@"(null)" ] || [self isEqualToString:@"(nil)"]) {
